# "for" é usado para percorrer uma coleção de elementos
fruits = ['maça', 'morango', 'banana', 'pera', 'melão']

for fruit in fruits #aqui o laco "for" está pegando todos os elementos do array "fruits" e 
    puts fruit      # jogando dentro da variável "fruit", ai ele printa na tela e retorna 
end                 # para o começo do laço, enquando existir frutas em fruits ele printa
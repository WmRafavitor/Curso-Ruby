# POO é um paradigma(um conjunto de conceitos usado para estruturar o código de forma 
#fique mais fácil de trabalhar) de programação criado para lidar com softwere  
#grandes e complexos. é um conceito seguido por várias liguagens de programação

#------------- OS 4 PILARES DA POO --------------------

#  - ABSTRAÇÃO
# A ação de abstrair uma entidade do mundo real e transforma-la em uma classe.


# - ENCAPSULAMENTO
# Ato de dividir programas em diversar partes tornando-o flexível, fácil de 
#modificar e incluir novas funcionalidades.


#  -  HERANÇA
# Habilidade de criar uma Classe com características de outra existente.
#A herança promove o reuso e reaproveitamento do código.


#  - POLIMORFISMO
# Capacidade de utilizar um método de diferentes formas para diferentes objetos